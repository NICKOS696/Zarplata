#!/bin/bash

# Скрипт для быстрого обновления приложения на сервере

echo "🔄 Обновление приложения..."

# Переходим в директорию проекта
cd /root/Zarplata

# Получаем последние изменения из Git
echo "📥 Получение изменений из Git..."
git pull

# Проверяем, были ли изменения
if [ $? -eq 0 ]; then
    echo "✅ Изменения получены успешно"
    
    # Перезапускаем контейнеры
    echo "🔄 Перезапуск контейнеров..."
    docker-compose down
    docker-compose up -d --build
    
    # Ждем запуска
    echo "⏳ Ожидание запуска..."
    sleep 10
    
    # Проверяем статус
    echo "📊 Статус контейнеров:"
    docker-compose ps
    
    echo ""
    echo "✨ Обновление завершено!"
    echo "🌐 Приложение доступно по адресу: http://[2a03:6f00:a::2:3be0]"
else
    echo "❌ Ошибка при получении изменений"
    exit 1
fi
